function date() {
	const date = new Date();
	const months = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'June', 'July', 'Aug', 'Sept', 'Oct', 'Nov', 'Dec'];
	const displayDate = {
		dd: date.getDate(),
		mmm: months[date.getMonth()],
		yyyy: date.getFullYear(),
		hh: date.getHours(),
		m: date.getMinutes(),
		ms: date.getMilliseconds()
    }
    smiley();
    console.log(dim, `LAST RUN: ${displayDate.dd}-${displayDate.mmm}-${displayDate.yyyy} ${displayDate.hh}:${displayDate.m}:${displayDate.ms}`);
    console.log(reset);
}

